# ==============================================================================
# 【Gitコマンド備忘録】
# ■ git checkout <ファイル名>
#   【ファイルの変更を完全に破棄】
#   最後にコミットした元の状態に戻す。（※作業中の変更は消えます）
#
# ■ git reset HEAD <ファイル名>
#   【コミット予定（ステージ）からの取り消し】
#   間違えて add したファイルをコミット対象から外す。（※ファイルの中身は残ります）
